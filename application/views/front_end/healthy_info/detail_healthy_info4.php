                    <p>Resep brunch yang mudah dan sehat
                    Bila Anda tidak sempat sarapan, bukan berarti Anda melewatkan jam makan Anda begitu saja. Nah, cobalah untuk membuat menu brunch yang mudah dan sehat agar nutrisi Anda tetap terpenuhi. Beragam menu yang cocok disantap saat brunch adalah sebagai berikut:</p>

                    <h4><b>1. Jus buah</b></h4>
                    <img src="https://hellosehat.com/wp-content/uploads/2018/03/shutterstock_562755607.jpg" width="600px">

                    <p>Menurut ahli gizi Fances Largeman-Roth, RD, dikutip dari Everyday Health, asupan cairan memengaruhi kebutuhan kalori Anda dalam seharian. Nah, Anda bisa membuat jus buah untuk memenuhi asupan energi dalam waktu singkat.</p>

                    <p><b>Sajian 2 porsi (111 kalori, 1 gram lemak, 2 gram protein, 24 gram karbohidrat per porsi)</b></p>

                    <p><b>Alat dan bahan:</b></p>
                    <p><ul>
                        <li>1 buah jeruk ukuran sedang</li>
                        <li>1 buah tomat, potong-potong</li>
                        <li>1 buah apel, potong menjadi 8 bagian</li>
                        <li>4 buah wortel</li>
                        <li>Air dan es secukupnya</li>
                    </ul></p>

                    <p><b>Cara membuat:</b></p>

                    <p>Masukkan semua bahan ke dalam blender. Haluskan hingga lembut dan mengental.
                    Tuang ke dalam gelas dan sajikan.</p>
                    
                    <h4><b>2. Pancake</b></h4>
                    <img src="https://hellosehat.com/wp-content/uploads/2018/03/shutterstock_244949971.jpg" width="600px">

                    <p>Pancake atau waffle merupakan makanan yang cocok disantap saat brunch. Buatlah pancake yang menggunakan biji-bijian utuh, atau yang lebih populer dengan nama whole grain, agar lebih padat nutrisinya. Kalaupun tidak, pancake pun sudah mengandung jumlah kalori yang seimbang bagi tubuh Anda.</p>

                    <p>Hindari penggunaan mentega, sirup, atau whipped cream untuk mengurangi kandungan gula dalam makanan. Sebaiknya gantilah dengan buah-buahan yang lebih segar yang jelas aman untuk dikonsumsi.</p>

                    <p><b>Sajian 4 porsi (189 kalori, 4 gram lemak, 6 gram protein, 34 gram karbohidrat per porsi)</b></p>

                    <p><b>Alat dan bahan:</b></p>

                   <p><ul>
                        <li>8 sdm buah stroberi, potong kasar</li>
                        <li>4 sdm tepung serbaguna</li>
                        <li>1 butir telur</li>
                        <li>2 sdm gula pasir</li>
                        <li>½ sdt baking powder</li>
                        <li>¼ sdt ekstrak vanili</li>
                        <li>6 sdm susu tanpa lemak</li>
                        <li>1½ sdt minyak canola</li>
                        <li>Garam secukupnya</li>
                        <li>Mangkuk kecil</li>
                        <li>Mangkuk besar</li>
                        <li>Wajan antilengket</li>
                    </ul></p>

                    <p><b>Cara membuat:</b></p>

                    <p><ul>
                        <li>1. Rebus 2 gelas air dalam panci kecil, kemudian masukkan buah stroberi. Rebus selama 2 menit, kemudian tiriskan selama 5 menit.</li>
                        <li>2. Siapkan mangkuk besar dan masukkan tepung serbaguna, gula pasir, baking powder, dan garam. Aduk hingga rata.</li>
                        <li>3. Siapkan mangkuk kecil dan masukkan susu, telur, minyak, ekstrak vanili, dan buah stroberi. Aduk hingga rata.</li>
                        <li>4. Masukkan seluruh adonan dalam mangkuk kecil ke dalam mangkuk besar, lalu aduk hingga rata.</li>
                        <li>5. Siapkan wajan antilengket dan panaskan dengan api sedang.</li>
                        <li>6. Masukkan 4 sdm adonan untuk masing-masing pancake, masak hingga muncul gelembung-gelembung kecil di permukaannya selama 2-3 menit.</li>
                        <li>7. Bolak-balik pancake selama 1-2 menit hingga kematangannya merata.</li>
                        <li>8. Sajikan selagi hangat, bisa juga ditambahkan madu.</li>
                    </ul></p>

                    <h4><b>3. Oatmeal</b></h4>
                    <img src="https://hellosehat.com/wp-content/uploads/2018/03/shutterstock_244259836.jpg" width="600px">


                    <p>Oatmeal dan granola bisa menjadi pilihan terbaik saat brunch. Namun, Anda harus berhati-hati. Beberapa produk oatmeal mengandung minyak, mentega, dan gula yang berlebihan. Jadi, pilih oatmeal polos dan tambahkan madu atau topping buah segar agar menambah cita rasa sekaligus menyehatkan.</p>

                    <p><b>Sajian 1 porsi (193 kalori, 3 gram lemak, 6 gram protein, 34 gram karbohidrat per porsi)</b></p>

                    <p><b>Alat dan bahan:</b></p>

                    <p><ul>
                        <li>4 gelas air</li>
                        <li>1 gelas oatmeal</li>
                        <li>3 sdm kismis</li>
                        <li>80 gram buah pisang</li>
                        <li>Garam secukupnya</li>
                    </ul></p>
                    <p><b>Cara membuat:</b></p>

                    <p><ul>
                        <li>1. Campurkan air, oat, kismis, dan garam ke dalam slow cooker.</li>
                        <li>2. Periksa tingkat airnya secara berkala. Sajikan dengan potongan pisang di atasnya.</li>
                    </ul></p>
